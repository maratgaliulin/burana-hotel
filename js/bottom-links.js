$(function(){
  $('#pill-10').on({
    mouseenter: function(){
      $('.pill10').css("background-color", "rgba(52, 51, 51, 0.7)");
      $('.pill10').css("color", "white");
    },
    mouseleave: function(){
      $('.pill10').css("background-color", "rgba(255, 255, 255, 0.7)");
      $('.pill10').css("color", "black");
    }
  })
});

